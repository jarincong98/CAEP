function [lhs, rhs] = static_resid(y, x, params)
T = NaN(7, 1);
lhs = NaN(8, 1);
rhs = NaN(8, 1);
T(1) = y(4)^params(2);
T(2) = y(2)^(-params(2));
T(3) = (1-params(2))*exp(y(3))*T(1)*T(2);
T(4) = 1/y(1)*params(3)/(1+params(4));
T(5) = params(2)*exp(y(3))*y(4)^(params(2)-1);
T(6) = y(2)^(1-params(2));
T(7) = 1+T(5)*T(6)-params(5);
lhs(1) = params(1)/(1-params(1))*y(1)/(1-y(2));
rhs(1) = T(3);
lhs(2) = 1/y(1);
rhs(2) = T(4)*T(7);
lhs(3) = y(4)*(1+params(4))*(1+params(6));
rhs(3) = y(4)*(1-params(5))+y(5);
lhs(4) = y(6);
rhs(4) = y(1)+y(5);
lhs(5) = y(6);
rhs(5) = T(6)*exp(y(3))*T(1);
lhs(6) = y(3);
rhs(6) = y(3)*params(7)+x(1);
lhs(7) = y(7);
rhs(7) = (1-params(2))*y(8);
lhs(8) = y(8);
rhs(8) = y(6)/y(2);
end
