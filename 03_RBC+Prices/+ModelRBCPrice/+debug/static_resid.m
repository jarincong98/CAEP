T(4) = params(8)^(1-params(7));
lhs(1) = y(7);
rhs(1) = 1;
lhs(2) = y(5);
rhs(2) = y(1)-y(1)*(1-params(3));
lhs(3) = y(4)*y(6)/y(16);
rhs(3) = y(2)^(-params(1));
lhs(4) = 1;
rhs(4) = T(1)/y(6);
lhs(5) = y(4)*y(6)/y(16);
rhs(5) = y(6)*y(4)*params(2)/y(16)*(1-params(3)+y(8));
lhs(6) = y(9);
rhs(6) = 1/y(14)*T(2)*T(3);
lhs(7) = y(10);
rhs(7) = 0;
lhs(8) = y(11);
rhs(8) = y(10)+y(5)+y(2);
lhs(9) = y(12);
rhs(9) = y(9);
lhs(10) = y(13);
rhs(10) = y(4)*y(11)-y(3)-y(1)*y(8)-y(10);
lhs(11) = y(9)*params(10)/y(4);
rhs(11) = params(10)-1;
lhs(12) = y(8);
rhs(12) = y(11)*params(4)*y(12)/y(1);
lhs(13) = 1;
rhs(13) = y(11)*(1-params(4))*y(12)/y(3);
lhs(14) = y(14);
rhs(14) = y(14)^params(7)*T(4)*(1+x(1));
lhs(15) = y(15);
rhs(15) = 0;
lhs(16) = y(16);
rhs(16) = y(16)^params(12)*(1+x(2));
end
